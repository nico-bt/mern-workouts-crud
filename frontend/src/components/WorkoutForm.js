import React, { useContext, useState } from 'react'
import { AuthContext } from '../context/AuthContext'
import useWorkoutsContext from '../hooks/useWorkoutsContext'

function WorkoutForm({setWorkouts}) {
    const [title, setTitle] = useState("")
    const [load, setLoad] = useState("")
    const [reps, setReps] = useState("")
    const [error, setError] = useState(null)
    const [emptyFields, setEmptyFields] = useState([])
    const { dispatch }  = useWorkoutsContext()
    const { state } = useContext(AuthContext)

    const handleSubmit = async (e)=>{
        e.preventDefault()

        // Can't submit if you are not logged in
        if(!state.token){ 
            return 
        }
        const newWorkout = {title, load, reps}
        
        // Send newWorkout to Database via a POST request
        const response = await fetch("http://localhost:4000/api/workouts", {
            method: "POST",
            body: JSON.stringify(newWorkout),
            headers: {
                "Content-Type": "application/json",
                "Authorization": `Bearer ${state.token}`
            }
        })
        const json = await response.json()

        if(!response.ok){
            setError(json.error)
            if(json.emptyFields) {
                setEmptyFields(json.emptyFields)
            }
        }
        if(response.ok){
            // Reset the form
            setTitle("")
            setLoad("")
            setReps("")
            setError(null)
            setEmptyFields([])
            // Pass the new workout to "Home" page, this will make it re-render without fetching all
            dispatch({
                type: "CREATE_WORKOUT",
                payload: json
            })
        }
    }

    return (
    <form className='create' onSubmit={handleSubmit}>
        <h3>Add a New Exercise</h3>

        <label>Excercise: </label>
        <input 
            type="text" 
            value={title} 
            onChange={(e)=>{setTitle(e.target.value)}}
            className= {emptyFields.includes("title")? "error" : ""}>
        </input>
        
        <label>Load (kg): </label>
        <input 
            type="number" 
            value={load} 
            onChange={(e)=>{setLoad(e.target.value)}}
            className= {emptyFields.includes("load")? "error" : ""}>
        </input>
        
        <label>Reps: </label>
        <input 
            type="number" min={1} 
            value={reps} 
            onChange={(e)=>{setReps(e.target.value)}}
            className= {emptyFields.includes("reps")? "error" : ""}>
        </input>

        <button type='submit'>Add Exercise</button>

        {error && <div className='error'>{error}</div>}
    </form>
  )
}

export default WorkoutForm